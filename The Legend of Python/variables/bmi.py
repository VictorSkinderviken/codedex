# Your weight in kilo
mass = 55
print ("You have entered a mass of", mass)

# Your height in Centimeter
height = 1.90
print ("You have entered a height of", height)

# Height times height
height = height ** 2
print ("Your height * height is", height)

# Formula for calculating BMI based on Height and mass is: 
# BMI = mass / height **2 
# OBS! (**2 means height to the power of 2)
bmi = mass / height
print ("That means your BMI is", bmi)
